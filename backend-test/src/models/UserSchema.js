const mongoose = require('mongoose')

const UserLoginSchema = new mongoose.Schema({
  firstName: {
    type: String,
    required: true,
    trim: true,
  },
  lastName: {
    type: String,
    required: true,
    default: true,
  },
  email: {
    type: String,
    required: true,
    unique: true,
    trim: true,
  },
  password: {
    type: String,
    required: true,
    default: true,
  },
})

module.exports = mongoose.model("User", UserLoginSchema)